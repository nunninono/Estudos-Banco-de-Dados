-- Criação de tabela
CREATE TABLE IF NOT EXISTS Funcionarios (
    ID_Funcionario INT PRIMARY KEY AUTO_INCREMENT,
    Nome VARCHAR(100) NOT NULL,
    Cargo VARCHAR(50),
    Salario DECIMAL(10, 2)
);

